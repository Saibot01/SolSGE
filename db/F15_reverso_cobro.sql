prompt == F15.7 PRC_RECHAZAR_REVERSO_COBRO ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_RECHAZAR_REVERSO_COBRO (
  p_id_solicitud   IN NUMBER,
  p_motivo_rechazo IN VARCHAR2,
  p_usuario        IN VARCHAR2
) IS
  v_estado CHAR(1);
BEGIN
  IF p_usuario IS NULL THEN
    RAISE_APPLICATION_ERROR(-20997,'Usuario aprobador requerido.');
  END IF;
  IF p_motivo_rechazo IS NULL OR LENGTH(TRIM(p_motivo_rechazo)) < 10 THEN
    RAISE_APPLICATION_ERROR(-20998,'El motivo de rechazo debe tener al menos 10 caracteres.');
  END IF;

  SELECT ESTADO INTO v_estado FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
   WHERE ID_SOLICITUD_RC = p_id_solicitud FOR UPDATE;
  IF v_estado <> 'P' THEN
    RAISE_APPLICATION_ERROR(-20996,'La solicitud no esta pendiente (ESTADO='||v_estado||').');
  END IF;

  UPDATE WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
     SET ESTADO='R', MOTIVO_RECHAZO=TRIM(p_motivo_rechazo),
         USUARIO_APRUEBA=p_usuario, FECHA_RESOLUCION=SYSDATE
   WHERE ID_SOLICITUD_RC = p_id_solicitud;
END PRC_RECHAZAR_REVERSO_COBRO;
