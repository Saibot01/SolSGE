prompt == F15.5 PRC_SOLICITAR_REVERSO_COBRO (crea solicitud 'P') ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_SOLICITAR_REVERSO_COBRO (
  p_id_movimiento IN  NUMBER,
  p_motivo        IN  VARCHAR2,
  p_usuario       IN  VARCHAR2,
  p_id_solicitud  OUT NUMBER
) IS
  v_bloqueo VARCHAR2(500);
BEGIN
  IF p_usuario IS NULL THEN
    RAISE_APPLICATION_ERROR(-20997,'Usuario solicitante requerido.');
  END IF;
  IF p_motivo IS NULL OR LENGTH(TRIM(p_motivo)) < 10 THEN
    RAISE_APPLICATION_ERROR(-20992,'El motivo del reverso debe tener al menos 10 caracteres.');
  END IF;

  v_bloqueo := WKSP_WORKPLACE.FN_COBRO_REVERSABLE(p_id_movimiento);
  IF v_bloqueo IS NOT NULL THEN
    RAISE_APPLICATION_ERROR(-20991, v_bloqueo);
  END IF;

  INSERT INTO WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO (
    ID_MOVIMIENTO, MOTIVO, ESTADO, USUARIO_SOLICITA, FECHA_SOLICITUD
  ) VALUES (
    p_id_movimiento, TRIM(p_motivo), 'P', p_usuario, SYSDATE
  ) RETURNING ID_SOLICITUD_RC INTO p_id_solicitud;
END PRC_SOLICITAR_REVERSO_COBRO;
/
