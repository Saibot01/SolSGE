-- ============================================================================
-- F15 - Reverso de Cobro de Cuota - workflow con aprobacion (4 ojos)
-- ============================================================================
-- Implementa PLAN_REVERSO_COBRO.md. Permite revertir un cobro de cuota ya
-- aplicado (FN_COBRAR_CUOTA de F9). Destraba dos flujos hoy bloqueados:
--   - F11 (anulacion): no se puede anular factura credito con cuota PAGADA.
--   - F14 (NC): NC de credito se capa al saldo; si excede lo cobrado -> -20982.
--
-- Decisiones (con el PO, 2026-06-24, ver plan §3):
--   1. Workflow con aprobacion: SOLICITA -> APRUEBA (efectos) / RECHAZA.
--   2. EGRESO en la caja abierta del dia del APROBADOR (espejo F11/F14).
--   3. La solicitud no toca caja ni CxC; solo al aprobar se materializan efectos.
--   4. Se conserva el recibo (nro RC quemado, no se decrementa el talonario).
--   5. Guard anti doble-reverso (indice unico parcial + re-check al aprobar).
--   6. Cuota reactivada -> VENCIDA si la fecha ya paso, si no PENDIENTE.
--
-- El EGRESO no lleva campos de recibo (quedan NULL) -> cumple CK_MOVCAJA_RECIBO.
-- CERRAR_CAJA v2 ya resta TIPO='EGRESO' del cierre del dia.
--
-- Rango de error reservado: -20991 .. -20999.
-- Idempotente: re-correrlo es no-op.
-- Pre-requisitos: F8 (MOVIMIENTOS_CAJA, FN_CAJA_ABIERTA_USUARIO) y F9
-- (FN_COBRAR_CUOTA, CUENTAS_COBRAR_DET con estados) aplicados.
--
-- Conexion: SQLCL_CONNECTION=tesis_db
-- Ejecucion: sql -S -name tesis_db @db/F15_reverso_cobro.sql
-- ============================================================================

