CREATE OR REPLACE VIEW WKSP_WORKPLACE.V_SOLICITUDES_REVERSO AS
SELECT s.ID_SOLICITUD_RC,
       s.ESTADO,
       s.MOTIVO,
       s.ID_MOVIMIENTO,
       mc.NRO_RECIBO,
       mc.FECHA              AS FECHA_COBRO,
       mc.TOTAL_MONEDA_LOCAL AS MONTO,
       mc.ID_CUENTA_COBRAR_DET,
       cd.ID_CXC,
       cd.NRO_CUOTA,
       cd.ESTADO             AS ESTADO_CUOTA,
       mc.ID_CLIENTE,
       TRIM(REGEXP_REPLACE(
         TRIM(BOTH ' ' FROM
           NVL(p.PRIMER_NOMBRE,'')||' '||NVL(p.SEGUNDO_NOMBRE,'')||' '||
           NVL(p.PRIMER_APELLIDO,'')||' '||NVL(p.SEGUNDO_APELLIDO,'')
         ), ' +', ' ')) AS CLIENTE_NOMBRE,
       s.USUARIO_SOLICITA, s.FECHA_SOLICITUD,
       s.USUARIO_APRUEBA, s.FECHA_RESOLUCION, s.MOTIVO_RECHAZO,
       s.ID_MOVIMIENTO_EGRESO
  FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO s
  JOIN WKSP_WORKPLACE.MOVIMIENTOS_CAJA mc ON mc.ID_MOVIMIENTO = s.ID_MOVIMIENTO
  LEFT JOIN WKSP_WORKPLACE.CUENTAS_COBRAR_DET cd ON cd.ID_DETALLE = mc.ID_CUENTA_COBRAR_DET
  LEFT JOIN WKSP_WORKPLACE.PERSONAS p ON p.ID_PERSONA = mc.ID_CLIENTE;

