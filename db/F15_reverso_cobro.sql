prompt == F15.4 FN_COBRO_REVERSABLE (bloqueos; NULL = se puede reversar) ==
CREATE OR REPLACE FUNCTION WKSP_WORKPLACE.FN_COBRO_REVERSABLE (
  p_id_movimiento IN NUMBER
) RETURN VARCHAR2 IS
  v_mov    WKSP_WORKPLACE.MOVIMIENTOS_CAJA%ROWTYPE;
  v_cnt    PLS_INTEGER;
  v_estcuo WKSP_WORKPLACE.CUENTAS_COBRAR_DET.ESTADO%TYPE;
  v_estcxc WKSP_WORKPLACE.CUENTAS_COBRAR.ESTADO%TYPE;
BEGIN
  BEGIN
    SELECT * INTO v_mov FROM WKSP_WORKPLACE.MOVIMIENTOS_CAJA
     WHERE ID_MOVIMIENTO = p_id_movimiento;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    RETURN 'El movimiento de caja indicado no existe.';
  END;

  IF v_mov.TIPO <> 'COBRO_CXC' THEN
    RETURN 'Solo se puede reversar un cobro de cuota (movimiento de tipo COBRO_CXC).';
  END IF;

  -- Ya reversado (solicitud aprobada) o con EGRESO de reversion ligado
  SELECT COUNT(*) INTO v_cnt FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
   WHERE ID_MOVIMIENTO = p_id_movimiento AND ESTADO = 'A';
  IF v_cnt > 0 THEN
    RETURN 'Este cobro ya fue reversado.';
  END IF;
  SELECT COUNT(*) INTO v_cnt FROM WKSP_WORKPLACE.MOVIMIENTOS_CAJA
   WHERE ID_MOVIMIENTO_REVERSADO = p_id_movimiento;
  IF v_cnt > 0 THEN
    RETURN 'Este cobro ya tiene un egreso de reversion registrado.';
  END IF;

  -- Solicitud pendiente en curso
  SELECT COUNT(*) INTO v_cnt FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
   WHERE ID_MOVIMIENTO = p_id_movimiento AND ESTADO = 'P';
  IF v_cnt > 0 THEN
    RETURN 'Ya existe una solicitud de reverso pendiente para este cobro.';
  END IF;

  -- Cuota / CxC asociada no debe estar anulada
  IF v_mov.ID_CUENTA_COBRAR_DET IS NOT NULL THEN
    BEGIN
      SELECT cd.ESTADO, cx.ESTADO INTO v_estcuo, v_estcxc
        FROM WKSP_WORKPLACE.CUENTAS_COBRAR_DET cd
        JOIN WKSP_WORKPLACE.CUENTAS_COBRAR     cx ON cx.ID_CXC = cd.ID_CXC
       WHERE cd.ID_DETALLE = v_mov.ID_CUENTA_COBRAR_DET;
      IF v_estcuo = 'ANULADA' OR v_estcxc = 'ANULADA' THEN
        RETURN 'La cuota o la cuenta por cobrar asociada esta anulada; no se puede reversar el cobro.';
      END IF;
    EXCEPTION WHEN NO_DATA_FOUND THEN
      RETURN 'No se encontro la cuota asociada al cobro.';
    END;
  END IF;

  RETURN NULL; -- reversable
END FN_COBRO_REVERSABLE;
