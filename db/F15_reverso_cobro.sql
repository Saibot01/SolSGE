prompt == F15.6 PRC_APROBAR_REVERSO_COBRO (materializa el reverso) ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_APROBAR_REVERSO_COBRO (
  p_id_solicitud IN NUMBER,
  p_usuario      IN VARCHAR2
) IS
  v_s        WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO%ROWTYPE;
  v_mov      WKSP_WORKPLACE.MOVIMIENTOS_CAJA%ROWTYPE;
  v_cuota    WKSP_WORKPLACE.CUENTAS_COBRAR_DET%ROWTYPE;
  v_cxc      WKSP_WORKPLACE.CUENTAS_COBRAR%ROWTYPE;
  v_n        PLS_INTEGER;
  v_ecuo     WKSP_WORKPLACE.CUENTAS_COBRAR_DET.ESTADO%TYPE;
  v_ecxc     WKSP_WORKPLACE.CUENTAS_COBRAR.ESTADO%TYPE;
  v_id_caja  NUMBER;
  v_fec_caja DATE;
  v_id_egreso NUMBER;
  v_nuevo_saldo NUMBER;
  v_nro_rec  VARCHAR2(40);
BEGIN
  IF p_usuario IS NULL THEN
    RAISE_APPLICATION_ERROR(-20997,'Usuario aprobador requerido.');
  END IF;

  -- 1) Lock solicitud, debe estar pendiente
  SELECT * INTO v_s FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
   WHERE ID_SOLICITUD_RC = p_id_solicitud FOR UPDATE;
  IF v_s.ESTADO <> 'P' THEN
    RAISE_APPLICATION_ERROR(-20996,'La solicitud no esta pendiente (ESTADO='||v_s.ESTADO||').');
  END IF;

  -- 2) Lock del cobro original y re-validar (concurrencia).
  -- NO se usa FN_COBRO_REVERSABLE aca: incluye el chequeo de "solicitud P", que
  -- aqui es justamente la solicitud en curso. Se re-chequean solo los guards
  -- duros: que no haya sido reversado por otra via y que la cuota/CxC no este anulada.
  SELECT * INTO v_mov FROM WKSP_WORKPLACE.MOVIMIENTOS_CAJA
   WHERE ID_MOVIMIENTO = v_s.ID_MOVIMIENTO FOR UPDATE;

  IF v_mov.TIPO <> 'COBRO_CXC' THEN
    RAISE_APPLICATION_ERROR(-20991,'El movimiento a reversar no es un cobro (COBRO_CXC).');
  END IF;
  SELECT COUNT(*) INTO v_n FROM WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
   WHERE ID_MOVIMIENTO = v_s.ID_MOVIMIENTO AND ESTADO='A';
  IF v_n > 0 THEN RAISE_APPLICATION_ERROR(-20991,'Este cobro ya fue reversado.'); END IF;
  SELECT COUNT(*) INTO v_n FROM WKSP_WORKPLACE.MOVIMIENTOS_CAJA
   WHERE ID_MOVIMIENTO_REVERSADO = v_s.ID_MOVIMIENTO;
  IF v_n > 0 THEN RAISE_APPLICATION_ERROR(-20991,'Este cobro ya tiene un egreso de reversion.'); END IF;
  IF v_mov.ID_CUENTA_COBRAR_DET IS NOT NULL THEN
    SELECT cd.ESTADO, cx.ESTADO INTO v_ecuo, v_ecxc
      FROM WKSP_WORKPLACE.CUENTAS_COBRAR_DET cd
      JOIN WKSP_WORKPLACE.CUENTAS_COBRAR     cx ON cx.ID_CXC = cd.ID_CXC
     WHERE cd.ID_DETALLE = v_mov.ID_CUENTA_COBRAR_DET;
    IF v_ecuo='ANULADA' OR v_ecxc='ANULADA' THEN
      RAISE_APPLICATION_ERROR(-20991,'La cuota o la cuenta asociada esta anulada; no se puede reversar.');
    END IF;
  END IF;
  v_nro_rec := v_mov.NRO_RECIBO;

  -- 3) Caja abierta del aprobador, del dia de hoy
  v_id_caja := WKSP_WORKPLACE.FN_CAJA_ABIERTA_USUARIO(p_usuario);
  IF v_id_caja IS NULL THEN
    RAISE_APPLICATION_ERROR(-20993,
      'Para aprobar un reverso necesitas tener caja abierta. Abri caja primero (P65).');
  END IF;
  SELECT FEC_APERTURA INTO v_fec_caja FROM WKSP_WORKPLACE.CAJAS WHERE ID_CAJA = v_id_caja;
  IF TRUNC(v_fec_caja) <> TRUNC(SYSDATE) THEN
    RAISE_APPLICATION_ERROR(-20994,
      'Tu caja abierta (#'||v_id_caja||') fue abierta el '||TO_CHAR(v_fec_caja,'DD/MM/YYYY')
      ||' y no corresponde al dia de hoy. Cerrala y abri una caja del dia.');
  END IF;

  -- 4) EGRESO de reversion (cabecera) en la caja del aprobador
  INSERT INTO WKSP_WORKPLACE.MOVIMIENTOS_CAJA (
    ID_CLIENTE, ID_CAJA, FECHA, TOTAL_MONEDA_LOCAL, MONEDA,
    TIPO_CAMBIO, TOTAL_MONEDA_ORIGEN, ESTADO, OBSERVACION,
    TIPO, USUARIO, ID_MOVIMIENTO_REVERSADO
  ) VALUES (
    v_mov.ID_CLIENTE, v_id_caja, SYSTIMESTAMP, v_mov.TOTAL_MONEDA_LOCAL, v_mov.MONEDA,
    v_mov.TIPO_CAMBIO, v_mov.TOTAL_MONEDA_ORIGEN, 'A',
    'Reverso de cobro recibo '||NVL(v_nro_rec,'(s/n)')||' - '||v_s.MOTIVO,
    'EGRESO', p_usuario, v_mov.ID_MOVIMIENTO
  ) RETURNING ID_MOVIMIENTO INTO v_id_egreso;

  -- 4b) Detalle espejo (forma/metodo de pago del cobro original)
  INSERT INTO WKSP_WORKPLACE.DETALLE_MOVIMIENTO_CAJA (
    ID_MOVIMIENTO, ID_FORMA_PAGO, ID_METODO_PAGO,
    MONTO_LOCAL, MONTO_ORIGEN, MONEDA, TIPO_CAMBIO, NRO_REFERENCIA
  )
  SELECT v_id_egreso, ID_FORMA_PAGO, ID_METODO_PAGO,
         MONTO_LOCAL, MONTO_ORIGEN, MONEDA, TIPO_CAMBIO, NRO_REFERENCIA
    FROM WKSP_WORKPLACE.DETALLE_MOVIMIENTO_CAJA
   WHERE ID_MOVIMIENTO = v_mov.ID_MOVIMIENTO;

  -- 5) Reactivar la cuota
  IF v_mov.ID_CUENTA_COBRAR_DET IS NOT NULL THEN
    SELECT * INTO v_cuota FROM WKSP_WORKPLACE.CUENTAS_COBRAR_DET
     WHERE ID_DETALLE = v_mov.ID_CUENTA_COBRAR_DET FOR UPDATE;

    UPDATE WKSP_WORKPLACE.CUENTAS_COBRAR_DET
       SET ESTADO = CASE WHEN FECHA_VENCIMIENTO < TRUNC(SYSDATE) THEN 'VENCIDA' ELSE 'PENDIENTE' END
     WHERE ID_DETALLE = v_mov.ID_CUENTA_COBRAR_DET;

    -- 6) Subir el saldo de la CxC
    SELECT * INTO v_cxc FROM WKSP_WORKPLACE.CUENTAS_COBRAR
     WHERE ID_CXC = v_cuota.ID_CXC FOR UPDATE;

    v_nuevo_saldo := NVL(v_cxc.SALDO,0) + v_mov.TOTAL_MONEDA_LOCAL;
    IF v_nuevo_saldo > v_cxc.TOTAL_A_PAGAR + 0.01 THEN
      RAISE_APPLICATION_ERROR(-20995,
        'El reverso dejaria el saldo ('||v_nuevo_saldo||') por encima del total de la cuenta ('
        ||v_cxc.TOTAL_A_PAGAR||'). Revise el cobro.');
    END IF;

    UPDATE WKSP_WORKPLACE.CUENTAS_COBRAR
       SET SALDO = v_nuevo_saldo, ESTADO = 'PENDIENTE'
     WHERE ID_CXC = v_cxc.ID_CXC;
  END IF;

  -- 7) Cerrar la solicitud
  UPDATE WKSP_WORKPLACE.SOLICITUDES_REVERSO_COBRO
     SET ESTADO='A', USUARIO_APRUEBA=p_usuario, FECHA_RESOLUCION=SYSDATE,
         ID_MOVIMIENTO_EGRESO=v_id_egreso
   WHERE ID_SOLICITUD_RC = p_id_solicitud;
END PRC_APROBAR_REVERSO_COBRO;
/
