DECLARE
  v_cnt PLS_INTEGER;
  v_ok  BOOLEAN := TRUE;
  PROCEDURE chk_obj(p_name VARCHAR2, p_type VARCHAR2) IS
  BEGIN
    SELECT COUNT(*) INTO v_cnt FROM all_objects
     WHERE owner='WKSP_WORKPLACE' AND object_name=p_name AND object_type=p_type AND status='VALID';
    IF v_cnt=1 THEN DBMS_OUTPUT.PUT_LINE('  OK  '||RPAD(p_type,10)||' '||p_name);
    ELSE DBMS_OUTPUT.PUT_LINE('  FAIL '||RPAD(p_type,10)||' '||p_name||' (count='||v_cnt||')'); v_ok:=FALSE; END IF;
  END;
BEGIN
  -- columna + FK
  SELECT COUNT(*) INTO v_cnt FROM all_tab_columns
   WHERE owner='WKSP_WORKPLACE' AND table_name='MOVIMIENTOS_CAJA' AND column_name='ID_MOVIMIENTO_REVERSADO';
  IF v_cnt=1 THEN DBMS_OUTPUT.PUT_LINE('  OK  COLUMN     MOVIMIENTOS_CAJA.ID_MOVIMIENTO_REVERSADO');
  ELSE DBMS_OUTPUT.PUT_LINE('  FAIL COLUMN     MOVIMIENTOS_CAJA.ID_MOVIMIENTO_REVERSADO'); v_ok:=FALSE; END IF;

  SELECT COUNT(*) INTO v_cnt FROM all_tables
   WHERE owner='WKSP_WORKPLACE' AND table_name='SOLICITUDES_REVERSO_COBRO';
  IF v_cnt=1 THEN DBMS_OUTPUT.PUT_LINE('  OK  TABLE      SOLICITUDES_REVERSO_COBRO');
  ELSE DBMS_OUTPUT.PUT_LINE('  FAIL TABLE      SOLICITUDES_REVERSO_COBRO'); v_ok:=FALSE; END IF;

  SELECT COUNT(*) INTO v_cnt FROM all_indexes
   WHERE owner='WKSP_WORKPLACE' AND index_name='UQ_SRC_MOV_PEND';
  IF v_cnt=1 THEN DBMS_OUTPUT.PUT_LINE('  OK  INDEX      UQ_SRC_MOV_PEND');
  ELSE DBMS_OUTPUT.PUT_LINE('  FAIL INDEX      UQ_SRC_MOV_PEND'); v_ok:=FALSE; END IF;

  chk_obj('FN_COBRO_REVERSABLE',          'FUNCTION');
  chk_obj('PRC_SOLICITAR_REVERSO_COBRO',  'PROCEDURE');
  chk_obj('PRC_APROBAR_REVERSO_COBRO',    'PROCEDURE');
