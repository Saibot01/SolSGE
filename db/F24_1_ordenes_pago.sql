-- ============================================================================
-- F24.1 - Orden de Pago (tablas + procedimientos)
-- ============================================================================
-- H2 del PLAN_CUENTAS_PAGAR.md. Modelo de DOS PASOS (decision PO 2026-07-02):
--   Paso 1  PRC_GENERAR_ORDEN_PAGO  -> emite la OP en BORRADOR (documento de
--           autorizacion; NO baja saldo). Aplica montos a una o varias CxP.
--   Paso 2  PRC_CONFIRMAR_ORDEN_PAGO -> ejecuta el pago: baja el SALDO de cada CxP
--           aplicada, la marca PAGADA/PARCIAL y la OP pasa a PAGADA.
--           SIN impacto en caja (fuera de alcance F24).
--           PRC_ANULAR_ORDEN_PAGO   -> anula una OP en BORRADOR (la reversa de una
--           OP ya PAGADA queda fuera de alcance).
--
-- Reusa la secuencia huerfana SEQ_ORDEN_PAGO_ID (scaffold que ya existia).
--
-- Rango de error: -20935 arg/monto invalido, -20936 CxP no corresponde al
--   proveedor, -20937 estado de OP invalido (no BORRADOR) al confirmar, -20938
--   saldo insuficiente al confirmar, -20939 no se puede anular / verificacion.
--
-- Los procedimientos NO hacen COMMIT: la transaccion la cierra el proceso APEX
-- que los invoca (o el smoke test con ROLLBACK).
--
-- Idempotente: tablas con IF NOT EXISTS; CREATE OR REPLACE para los procs.
-- Pre-requisitos: CUENTAS_PAGAR (F24 H1), PROVEEDORES, METODOS_PAGO, FN_AHORA,
--                 SEQ_ORDEN_PAGO_ID.
--
-- Conexion: SQLCL_CONNECTION=tesis_db
-- Ejecucion (esta maquina): sql -S -name tesis_db < db/F24_1_ordenes_pago.sql
-- ============================================================================

