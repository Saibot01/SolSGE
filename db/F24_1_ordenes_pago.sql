prompt == F24.1.3 PRC_GENERAR_ORDEN_PAGO (paso 1: emite BORRADOR, no baja saldo) ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_GENERAR_ORDEN_PAGO (
  p_id_proveedor  IN  NUMBER,
  p_ids_cxp       IN  SYS.ODCINUMBERLIST,
  p_montos        IN  SYS.ODCINUMBERLIST,
  p_observacion   IN  VARCHAR2 DEFAULT NULL,
  p_usuario       IN  VARCHAR2 DEFAULT NULL,
  p_id_orden_pago OUT NUMBER
) AS
  v_total NUMBER := 0;
  v_saldo NUMBER;
  v_cnt   PLS_INTEGER;
BEGIN
  IF p_id_proveedor IS NULL THEN
    RAISE_APPLICATION_ERROR(-20935,'Falta el proveedor.');
  END IF;
  IF p_ids_cxp IS NULL OR p_ids_cxp.COUNT = 0 THEN
    RAISE_APPLICATION_ERROR(-20935,'No hay cuentas por pagar seleccionadas.');
  END IF;
  IF p_montos IS NULL OR p_montos.COUNT <> p_ids_cxp.COUNT THEN
    RAISE_APPLICATION_ERROR(-20935,'La cantidad de montos no coincide con las cuentas.');
  END IF;

  SELECT COUNT(*) INTO v_cnt FROM WKSP_WORKPLACE.PROVEEDORES
   WHERE ID_PERSONA = p_id_proveedor;
  IF v_cnt = 0 THEN
    RAISE_APPLICATION_ERROR(-20935,'Proveedor inexistente: '||p_id_proveedor);
  END IF;

  -- Cabecera BORRADOR: autoriza, NO baja saldo (paso 1 del modelo de dos pasos).
  INSERT INTO WKSP_WORKPLACE.ORDENES_PAGO (
    ID_PROVEEDOR, FECHA_EMISION, ESTADO, USUARIO, OBSERVACION
  ) VALUES (
    p_id_proveedor, WKSP_WORKPLACE.FN_AHORA, 'BORRADOR',
    NVL(p_usuario, USER), p_observacion
  ) RETURNING ID_ORDEN_PAGO INTO p_id_orden_pago;

  FOR i IN 1 .. p_ids_cxp.COUNT LOOP
    IF p_montos(i) IS NULL OR p_montos(i) <= 0 THEN
      RAISE_APPLICATION_ERROR(-20935,'Monto invalido para la CxP '||p_ids_cxp(i)||'.');
    END IF;
    -- La CxP debe existir, ser del proveedor y seguir pendiente/parcial.
    BEGIN
      SELECT SALDO INTO v_saldo
        FROM WKSP_WORKPLACE.CUENTAS_PAGAR
       WHERE ID_CXP = p_ids_cxp(i)
         AND ID_PROVEEDOR = p_id_proveedor
         AND ESTADO NOT IN ('PAGADA','ANULADA');
    EXCEPTION WHEN NO_DATA_FOUND THEN
      RAISE_APPLICATION_ERROR(-20936,'La CxP '||p_ids_cxp(i)||
        ' no corresponde al proveedor o no esta pendiente.');
    END;
    IF p_montos(i) > v_saldo THEN
      RAISE_APPLICATION_ERROR(-20935,'El monto '||p_montos(i)||' supera el saldo '||
        v_saldo||' de la CxP '||p_ids_cxp(i)||'.');
    END IF;

    INSERT INTO WKSP_WORKPLACE.ORDEN_PAGO_DET (ID_ORDEN_PAGO, ID_CXP, MONTO_APLICADO)
    VALUES (p_id_orden_pago, p_ids_cxp(i), p_montos(i));

    v_total := v_total + p_montos(i);
  END LOOP;

  UPDATE WKSP_WORKPLACE.ORDENES_PAGO
     SET TOTAL_PAGO = v_total
   WHERE ID_ORDEN_PAGO = p_id_orden_pago;
END PRC_GENERAR_ORDEN_PAGO;
