prompt == F24.1.5 PRC_ANULAR_ORDEN_PAGO (solo BORRADOR) ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_ANULAR_ORDEN_PAGO (
  p_id_orden_pago IN NUMBER,
  p_motivo        IN VARCHAR2 DEFAULT NULL
) AS
  v_estado VARCHAR2(20);
BEGIN
  BEGIN
    SELECT ESTADO INTO v_estado FROM WKSP_WORKPLACE.ORDENES_PAGO
     WHERE ID_ORDEN_PAGO = p_id_orden_pago FOR UPDATE;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20939,'Orden de pago inexistente: '||p_id_orden_pago);
  END;
  IF v_estado <> 'BORRADOR' THEN
    RAISE_APPLICATION_ERROR(-20939,'Solo se anula una OP en BORRADOR (estado actual: '||
      v_estado||'). Reversar una OP pagada esta fuera de alcance.');
  END IF;
  UPDATE WKSP_WORKPLACE.ORDENES_PAGO
     SET ESTADO = 'ANULADA', MOTIVO_ANULACION = p_motivo
   WHERE ID_ORDEN_PAGO = p_id_orden_pago;
END PRC_ANULAR_ORDEN_PAGO;
/
show errors procedure PRC_ANULAR_ORDEN_PAGO

