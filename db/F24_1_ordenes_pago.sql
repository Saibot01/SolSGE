prompt == F24.1.4 PRC_CONFIRMAR_ORDEN_PAGO (paso 2: ejecuta el pago, baja saldo) ==
CREATE OR REPLACE PROCEDURE WKSP_WORKPLACE.PRC_CONFIRMAR_ORDEN_PAGO (
  p_id_orden_pago  IN NUMBER,
  p_id_metodo_pago IN NUMBER DEFAULT NULL
) AS
  v_estado VARCHAR2(20);
  v_saldo  NUMBER;
  v_nuevo  NUMBER;
  v_cnt    PLS_INTEGER;
BEGIN
  -- Bloquea la cabecera y valida el estado.
  BEGIN
    SELECT ESTADO INTO v_estado FROM WKSP_WORKPLACE.ORDENES_PAGO
     WHERE ID_ORDEN_PAGO = p_id_orden_pago FOR UPDATE;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20937,'Orden de pago inexistente: '||p_id_orden_pago);
  END;
  IF v_estado <> 'BORRADOR' THEN
    RAISE_APPLICATION_ERROR(-20937,'Solo se confirma una OP en BORRADOR (estado actual: '||v_estado||').');
  END IF;

  IF p_id_metodo_pago IS NOT NULL THEN
    SELECT COUNT(*) INTO v_cnt FROM WKSP_WORKPLACE.METODOS_PAGO
     WHERE ID_METODO_PAGO = p_id_metodo_pago;
    IF v_cnt = 0 THEN
      RAISE_APPLICATION_ERROR(-20935,'Metodo de pago invalido: '||p_id_metodo_pago);
    END IF;
  END IF;

  -- Aplica cada detalle: bloquea la CxP, revalida saldo (concurrencia) y lo baja.
  FOR r IN (SELECT ID_CXP, MONTO_APLICADO FROM WKSP_WORKPLACE.ORDEN_PAGO_DET
             WHERE ID_ORDEN_PAGO = p_id_orden_pago) LOOP
    SELECT SALDO INTO v_saldo FROM WKSP_WORKPLACE.CUENTAS_PAGAR
     WHERE ID_CXP = r.ID_CXP FOR UPDATE;
    IF r.MONTO_APLICADO > v_saldo THEN
      RAISE_APPLICATION_ERROR(-20938,'Saldo insuficiente en la CxP '||r.ID_CXP||
        ' (saldo '||v_saldo||', aplica '||r.MONTO_APLICADO||').');
    END IF;
    v_nuevo := v_saldo - r.MONTO_APLICADO;
    UPDATE WKSP_WORKPLACE.CUENTAS_PAGAR
       SET SALDO  = v_nuevo,
           ESTADO = CASE WHEN v_nuevo <= 0 THEN 'PAGADA' ELSE 'PARCIAL' END
     WHERE ID_CXP = r.ID_CXP;
  END LOOP;

  UPDATE WKSP_WORKPLACE.ORDENES_PAGO
     SET ESTADO = 'PAGADA',
         FECHA_PAGO = WKSP_WORKPLACE.FN_AHORA,
         ID_METODO_PAGO = p_id_metodo_pago
   WHERE ID_ORDEN_PAGO = p_id_orden_pago;
END PRC_CONFIRMAR_ORDEN_PAGO;
/
